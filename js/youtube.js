function blockVideo(element) {
  let channelName = element.querySelector("#channel-name a");
  if (!channelName) {
    // console.log("not a");
    channelName = element.getElementsByTagName("yt-lockup-metadata-view-model");
    if (channelName.length > 0) {
      channelName = channelName[0];
      channelName = channelName.querySelector(".yt-lockup-metadata-view-model-wiz__text-container yt-content-metadata-view-model>div>span");
      // console.log("yes b");
    }
    if (!channelName || channelName === null) {
      // channelName = null;
      let anchors = element.getElementsByTagName("a");
      channelName = Array.from(anchors).find(x => x.href.includes("/@"));
      if (!channelName) {
        // console.log("not c");
        channelName = null;
      }
    }
  }
  // console.log("got channel name", channelName);
  channelName =
    channelName?.textContent || channelName?.text || channelName?.title;
  channelName = cleanText(channelName);
  let videoTitle = element.querySelector("#video-title");
  if (!videoTitle) {
    videoTitle = element.querySelector("h3");
  }
  videoTitle = videoTitle?.textContent || videoTitle?.text || videoTitle?.title;
  videoTitle = cleanText(videoTitle);
  if (!videoTitle) return;

  let match = matches(videoTitle);
  if (!match.match) match = matches(channelName);

  let existingBackdrop = element.querySelector(`.${BACKDROP_CLASSNAME}`); //|| queryParentElement(element, `.${BACKDROP_CLASSNAME}`);
  if (existingBackdrop && !match.match) {
    existingBackdrop.remove();
  }
  if (!match.match || existingBackdrop) return;
  console.log(`Blocking ${channelName} : ${videoTitle} ${element.tagName}`);
  let backdrop = newBackdropElement(element, match.regex);
  element.toggleAttribute(`${BACKDROP_CLASSNAME}-blocked`);
  element.style.position = "relative";
  element.appendChild(backdrop);
}

