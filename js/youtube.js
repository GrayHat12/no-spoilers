function reset() {
  let elements = document.querySelectorAll(`.${BACKDROP_CLASSNAME}`);
  Array.from(elements).forEach((element) => {
    let parent = element.closest(`[${BACKDROP_CLASSNAME}-blocked]`);
    if (parent) {
      parent.toggleAttribute(`${BACKDROP_CLASSNAME}-blocked`);
    }
    element.remove();
  });
  completeExistingContent();
}

/**
 * 
 * @param {string[]} keywords 
 * @param {boolean} enabled 
 */
function update(keywords, enabled, showBlockedKeywords) {
  KEYWORDS = keywords;
  ENABLE = enabled;
  SHOW_BLOCKED_KEYWORD = showBlockedKeywords;
  REGEXES = KEYWORDS.map((keyword) => {
    let regex = keyword.replace(/[^\w\d\s]/gim, ".*");
    regex = regex.replace(/\s+/gim, "[\\s\\.-_]*");
    regex = regex.replace(/\d+/gim, "\\d+");
    regex = `[^\\w\\d]*${regex}`;
    return { keyword, regex: new RegExp(regex, "im") };
  });
  console.log({ REGEXES });
  reset();
}

