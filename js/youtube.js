/**
 * 
 * @param {string} text 
 */
function matches(text) {
  if (!text) return false;
  for (let regex of REGEXES) {
    if (text.search(regex.regex) != -1) {
      return { match: true, regex: regex };
    }
  }
  return { match: false };
}

/**
 * 
 * @param {string} text 
 * @returns {string}
 */
function cleanText(text) {
  if (!text) return text;
  if (typeof text === "string" || text instanceof String) {
    return text.replace("\n", "").replace(/\s+/gim, " ");
  }
  return text;
}

/**
 * 
 * @param {HTMLElement} el 
 * @param {string} selector 
 */
function queryParentElement(el, selector) {
  if (!el) return el;
  if (el.parentElement?.querySelector(selector)) return el;
  else return queryParentElement(el.parentElement, selector);
}

/**
 * @param {HTMLElement} parentElement 
 * @returns {HTMLButtonElement}
 */
function createToogleVisibilityButton(parentElement) {
  const button = document.createElement("button");

  // Add our targeted CSS class
  button.className = `${BACKDROP_CLASSNAME}-toggle-btn`;

  // YouTube-style button aesthetics
  Object.assign(button.style, {
    background: "rgba(0, 0, 0, 0.6)", // Dark translucent pill/circle
    border: "none",
    color: "#f1f1f1", // YouTube standard off-white
    fill: "#f1f1f1",
    cursor: "pointer",
    borderRadius: "50%", // Circular button
    padding: "8px",
    display: "flex",
    alignItems: "center",
    justifyContent: "center",
    transition: "background-color 0.2s ease",
    width: "40px",
    height: "40px"
  });

  const formatIcon = (svgString) => svgString.replace('width="32" height="32"', 'width="20" height="20"');

  button.addEventListener("mouseenter", () => button.style.backgroundColor = "rgba(0, 0, 0, 0.8)");
  button.addEventListener("mouseleave", () => button.style.backgroundColor = "rgba(0, 0, 0, 0.6)");

  const isRevealed = parentElement.classList.contains("is-revealed");
  button.innerHTML = isRevealed ? formatIcon(VISIBLE_ICON) : formatIcon(HIDDEN_ICON);

  button.addEventListener("click", (ev) => {
    ev.preventDefault();
    ev.stopPropagation();

    const parent = button.parentElement;

    parent.classList.toggle("is-revealed");

    const currentlyRevealed = parent.classList.contains("is-revealed");
    button.innerHTML = currentlyRevealed ? formatIcon(VISIBLE_ICON) : formatIcon(HIDDEN_ICON);

    button.title = currentlyRevealed ? "Hide video" : "Show video";
  });

  return button;
}

