function blockElement(element) {
  try {
    if (!element) {
      return;
    }
    if (!ENABLE) {
      return;
    }
    if (element.querySelector(`.${BACKDROP_CLASSNAME}`) || element.hasAttribute(`${BACKDROP_CLASSNAME}-blocked`) || element.closest(`[${BACKDROP_CLASSNAME}-blocked]`)) {
      return;
    }
    // if (element.tagName.startsWith("ytm")) blockShorts(element);
    // else blockVideo(element);
    blockVideo(element);
  } catch (err) {
    console.error(err);
  }
}

